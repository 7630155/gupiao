package ddq.model;

import java.io.Serializable;
import java.util.*;

public class Stock implements Serializable{
	private static final long serialVersionUID = 5138984773718311001L;
	/*
	 * 
	Id int NOT NULL AUTO_INCREMENT,
	stockid varchar(10),
    closeprice double COMMENT '当前价格',
	market varchar(10) COMMENT '市场',
	updatedate timestamp COMMENT '更新时间',
	createdate timestamp COMMENT '创建时间',
    company varchar(10) COMMENT '上市公司',
    name varchar(10)
	 */
	
	int id;
	String stockid;
	double closeprice;
	String market;
	Date updatedate;
	Date createdate;
	String company;
	String name;
	String marketname;
	String firstspell;
	String spell;
	String type;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getStockid() {
		return stockid;
	}
	public void setStockid(String stockid) {
		this.stockid = stockid;
	}
	public double getCloseprice() {
		return closeprice;
	}
	public void setCloseprice(double closeprice) {
		this.closeprice = closeprice;
	}
	public String getMarket() {
		return market;
	}
	public void setMarket(String market) {
		this.market = market;
	}
	public Date getUpdatedate() {
		return updatedate;
	}
	public void setUpdatedate(Date updatedate) {
		this.updatedate = updatedate;
	}
	public Date getCreatedate() {
		return createdate;
	}
	public void setCreatedate(Date createdate) {
		this.createdate = createdate;
	}
	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getMarketname() {
		return marketname;
	}
	public void setMarketname(String marketname) {
		this.marketname = marketname;
	}
	public String getFirstspell() {
		return firstspell;
	}
	public void setFirstspell(String firstspell) {
		this.firstspell = firstspell;
	}
	public String getSpell() {
		return spell;
	}
	public void setSpell(String spell) {
		this.spell = spell;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	

}
